	public function is_invalid($password) {
		if (!$password) 
			return "Empty Password";
		$parray = str_split(strtolower($password));
		if (!$parray) {
			return "Invalid data given to validator";
		}
		
		foreach ($parray as $k=>$c) {
			switch ($c) {
				case '$':
					$parray[$k] = '[\$s]';
					break;
				case '@':
					$parray[$k] = '[@a]';
					break;
				case '0':
					$parray[$k] = '[0o]';
					break;
